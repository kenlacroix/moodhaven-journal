/**
 * ContextMenu - Dropdown menu for adding context to entries
 *
 * Per spec:
 * - Contains: Attachment, Location, Photo, Link, Reflect with AI
 * - Hidden unless explicitly opened
 * - Closes on blur
 * - Does not resize the editor
 * - Does not steal focus unless clicked
 */

import { useEffect, useRef } from 'react';

interface ContextMenuProps {
  position?: { top: number; left: number };
  onAction: (action: string) => void;
  onClose: () => void;
}

const MENU_ITEMS = [
  { id: 'attachment', label: 'Attachment', icon: AttachmentIcon },
  { id: 'location', label: 'Location', icon: LocationIcon },
  { id: 'photo', label: 'Photo', icon: PhotoIcon },
  { id: 'link', label: 'Link', icon: LinkIcon },
  { id: 'reflect', label: 'Reflect with AI', icon: AIIcon },
];

export function ContextMenu({ position, onAction, onClose }: ContextMenuProps) {
  const menuRef = useRef<HTMLDivElement>(null);

  // Close on escape
  useEffect(() => {
    const handleKeyDown = (e: KeyboardEvent) => {
      if (e.key === 'Escape') {
        onClose();
      }
    };

    document.addEventListener('keydown', handleKeyDown);
    return () => document.removeEventListener('keydown', handleKeyDown);
  }, [onClose]);

  return (
    <div
      ref={menuRef}
      className="absolute z-50 w-48 py-1 bg-white dark:bg-slate-800 rounded-lg shadow-xl border border-slate-200 dark:border-slate-700"
      style={position ? { top: position.top, left: position.left } : {}}
    >
      {MENU_ITEMS.map((item) => (
        <button
          key={item.id}
          type="button"
          onClick={() => onAction(item.id)}
          className="
            w-full flex items-center gap-3 px-3 py-2 text-left text-sm
            text-slate-700 dark:text-slate-200
            hover:bg-slate-100 dark:hover:bg-slate-700
            transition-colors
          "
        >
          <item.icon className="w-4 h-4 text-slate-400 dark:text-slate-500" />
          <span>{item.label}</span>
        </button>
      ))}
    </div>
  );
}

